    private void GetProvider()
    {
        try
        {
            String sql = "select * from Provider";
            st = con.createStatement();
            rs = st.executeQuery(sql);
        while(rs.next())
        {
            cmbProvider.addItem(rs.getString("NAME"));
        }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
