    private void GetDisease()
    {
        String sql = "Select * from Disease";
        try
        {
            //st = con.createStatement();
            rs = con.createStatement().executeQuery(sql);
            while(rs.next())
            {
                cmbDisease.addItem(rs.getString("Name"));
            }
        }catch (Exception e)
        {
            e.printStackTrace();
        }
        
    }
    private void InitQuery()
    {
        sqlMed = "select Disease.Name,Medicine.Name as MedName \n" +
"from (Medicine inner join Medication on Medicine.[Medicine#]= Medication.[Medicine#]) inner join Disease on Medication.[Disease.ID]=Disease.ID\n" +
"where Disease.Name='"+cmbDisease.getSelectedItem().toString()+"'";
        sqlTreatment = "select Disease.Name,Treatment.Name as TreatName \n" +
"from (Treatment inner join TreatmentDisease on Treatment.[Treatment#]= TreatmentDisease.[Treatment#]) inner join Disease on TreatmentDisease.[Disease.ID]=Disease.ID\n" +
"where Disease.Name='"+cmbDisease.getSelectedItem()+"'";
    }
