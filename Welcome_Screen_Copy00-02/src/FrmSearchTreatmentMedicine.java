    private void cmbDiseaseItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDiseaseItemStateChanged
        // TODO add your handling code here:
        mdlTbMed.setRowCount(0);
        mdlTbTreat.setRowCount(0);
        try {
            // TODO add your handling code here:
            ResultSetMetaData rsmdmed, rsmdtrt;
            InitQuery();
            rsMed = con.createStatement().executeQuery(sqlMed);
            rsTreatment = con.createStatement().executeQuery(sqlTreatment);
            rsmdmed = rsMed.getMetaData();
            rsmdtrt = rsTreatment.getMetaData();
            while(rsMed.next())
            {
                Vector<String> lst = new Vector<String>();
                lst.add(rsMed.getString(rsmdmed.getColumnName(2)));
                mdlTbMed.addRow(lst);
            }
            while(rsTreatment.next())
            {
                Vector<String> lst = new Vector<String>();
                lst.add(rsTreatment.getString(rsmdtrt.getColumnName(2)));
                mdlTbTreat.addRow(lst);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmSearchTreatmentMedicine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbDiseaseItemStateChanged

