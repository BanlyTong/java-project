    private void btnInDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInDeleteActionPerformed
        // TODO add your handling code here:
        int i=tbInsurance.getSelectedRow();
        model.removeRow(i);
        boolean d=false;
        try{
            rs.absolute(i+1);
            rs.deleteRow();
            d=rs.rowDeleted();

        }catch(Exception ex){
            Logger.getLogger(InsuranceTab.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null,"Deleted= "+d);
        refreshData();
    }//GEN-LAST:event_btnInDeleteActionPerformed

