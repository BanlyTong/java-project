    private void btnInSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInSaveActionPerformed
        if(btnInSave.getText().equalsIgnoreCase("Save")){
            try {
                // TODO add your handling code here:
                String id=txtID.getText();
                String name=txtName.getText();
                String add=txtAddress.getText();
                rs.moveToInsertRow();
                rs.updateString("ID", id);
                rs.updateString("Name",name);
                rs.updateString("Addresss",add);
                rs.insertRow();
                insertRowToTable(id,name,add);
            } catch (SQLException ex) {
                Logger.getLogger(InsuranceTab.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if(btnInSave.getText().equalsIgnoreCase("Update")){
            int index=tbInsurance.getSelectedRow();
            String name=txtName.getText();
            String add=txtAddress.getText();
            try{
                rs.absolute(index+1);
                rs.updateString("Name", name);
                rs.updateString("Addresss", add);
                rs.updateRow();
                model.setValueAt(name, index, 1);
                model.setValueAt(add,index,2);
            }catch(Exception ex){
                Logger.getLogger(InsuranceTab.class.getName()).log(Level.SEVERE, null, ex);
            }
            btnInSave.setText("Save");

        }
        clearAllTextFields();
    }//GEN-LAST:event_btnInSaveActionPerformed

